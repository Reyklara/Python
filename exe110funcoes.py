

def soma(a,b):
    soma =  a + b
    print(f"A soma de {a} + {b} = {soma}")

def subt(a,b):
    subt =  a - b
    print(f"A subtração de {a} - {b} = {subt}")

def mult(a,b):
    mult =  a * b
    print(f"A multiplicação de {a} * {b} = {mult}")

def divi(a,b):
    divi =  a / b
    print(f"A divisão de {a} / {b} = {divi}")

       

    
        
   
        

   
        